    private void PrevActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PrevActionPerformed
        clearText();
        if (evt.getSource() == Prev) {
            clearText();
            if (check != 0) {
                if (check == split.length) {
                    check--;
                    NIM.setText(nim.get(check - 1));
                    Nama.setText(nama.get(check - 1));
                    Alamat.setText(alamat.get(check - 1));
                    check--;
                } else if (check > 0) {
                    check--;
                    NIM.setText(nim.get(check));
                    Nama.setText(nama.get(check));
                    Alamat.setText(alamat.get(check));

                }
            } else {
                NIM.setText(nim.get(check));
                Nama.setText(nama.get(check));
                Alamat.setText(alamat.get(check));
            }
        }
    }//GEN-LAST:event_PrevActionPerformed

