    private void BacaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BacaActionPerformed
        // TODO add your handling code here:
        if (jTextField4.isValid()) {
            ObjectInputStream is;
            try {
                is = new ObjectInputStream(new FileInputStream(jTextField4.getText()));
                String dt = (String) is.readObject();

                split = dt.split(";");
                for (counter = 0; counter < split.length; counter++) {
                    StringTokenizer st = new StringTokenizer(split[counter], "+");
                    while (st.hasMoreTokens()) {
                        nim.add(st.nextToken());
                        nama.add(st.nextToken());
                        alamat.add(st.nextToken());
                    }
                }
                check = 0;
                NIM.setText(nim.get(check));
                Nama.setText(nama.get(check));
                Alamat.setText(alamat.get(check));
            } catch (Exception ioe) {
                System.err.println("Error " + ioe);
            }
        }


    }//GEN-LAST:event_BacaActionPerformed

    private void NextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NextActionPerformed
        if (evt.getSource() == Next) {
            clearText();
            if (check != split.length) {
                if (check == 0) {
                    check++;
                    NIM.setText(nim.get(check));
                    Nama.setText(nama.get(check));
                    Alamat.setText(alamat.get(check));
                } else if (check < split.length) {
                    NIM.setText(nim.get(check));
                    Nama.setText(nama.get(check));
                    Alamat.setText(alamat.get(check));
                }
                check++;
            } else {
                NIM.setText(nim.get(split.length - 1));
                Nama.setText(nama.get(split.length - 1));
                Alamat.setText(alamat.get(split.length - 1));
            }
        }
    }//GEN-LAST:event_NextActionPerformed

